	<h1>El verbo "to be" en inglés</h1>
	<p>El verbo "to be" en inglés es uno de los verbos más importantes y se utiliza para formar oraciones en presente, pasado y futuro. A continuación, te mostramos una tabla con las distintas formas del verbo y sus usos más comunes:</p>

	<table>
		<tr>
			<th>Forma</th>
			<th>Uso</th>
			<th>Ejemplo</th>
		</tr>
		<tr>
			<td>am</td>
			<td>Para la primera persona del singular en presente (I am)</td>
			<td>I am a student.</td>
		</tr>
		<tr>
			<td>is</td>
			<td>Para la tercera persona del singular en presente (he/she/it is)</td>
			<td>He is tall.</td>
		</tr>
		<tr>
			<td>are</td>
			<td>Para la segunda persona del singular y plural en presente (you/we/they are)</td>
			<td>You are my friend.</td>
		</tr>
		<tr>
			<td>was</td>
			<td>Para todas las personas en pasado (was/were)</td>
			<td>I was at the beach yesterday.</td>
		</tr>
		<tr>
			<td>were</td>
			<td>Para la segunda persona del singular y plural en pasado (you/we/they were)</td>
			<td>You were late yesterday.</td>
		</tr>
		<tr>
			<td>will be</td>
			<td>Para todas las personas en futuro</td>
			<td>I will be at the party tomorrow.</td>
		</tr>
	</table>

	<p>Recuerda que el verbo "to be" es irregular, lo que significa que no sigue las reglas regulares de conjugación de verbos. Por lo tanto, es importante que practiques su uso para poder utilizarlo correctamente en tus oraciones en inglés.</p>
